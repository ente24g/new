import random  # Імпортуємо модуль random для генерації випадкового числа

# Генеруємо випадкове число від 1 до 10
secret_number = random.randint(1, 10)  #randint – це функція з модуля random, яка генерує випадкове ціле число в заданому діапазоні.

print("Я загадав число від 1 до 10. Спробуй вгадати!")

while True:  # Безкінечний цикл, поки користувач не вгадає число
    try:
        guess = int(input("Введи своє число: "))  # Отримуємо число від користувача

        if guess < 1 or guess > 10:  # Перевіряємо, чи число в діапазоні
            print("Будь ласка, введіть число від 1 до 10.")
            continue

        if guess == secret_number:  # Якщо користувач вгадав
            print("Вітаю! Ти вгадав число!")
            break  # Виходимо з циклу
        elif guess < secret_number:  # Якщо число замале
            print("Ні, моє число більше.")
        else:  # Якщо число завелике
            print("Ні, моє число менше.")

    except ValueError:  # Обробляємо випадок, коли введено не число
        print("Будь ласка, введіть коректне число.")
